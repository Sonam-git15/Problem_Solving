package Strings;

public class StringFloat {
    public static void main(String[] args) {

        // Placeholder Values
        float dec = 456.1274f;
        // Use %f
        System.out.printf("Formatted number is %.2f", dec);
        System.out.println();

        System.out.println("Pie = " + Math.PI);

        System.out.printf("pie: %.3f", Math.PI);
        System.out.println();

        //Use %s
        System.out.printf("Hello my name is %s and I am %s Person", "Sonam", "Cool");

        
    }
    
}
